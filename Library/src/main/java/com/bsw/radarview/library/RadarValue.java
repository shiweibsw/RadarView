package com.bsw.radarview.library;

public class RadarValue{
        private String name;
        private int value;
        public RadarValue(){
        }
        public String getName() {
            return name;
        }

        public void setName(String name) {
            this.name = name;
        }

        public int getValue() {
            return value;
        }

        public void setValue(int value) {
            this.value = value;
        }
    }